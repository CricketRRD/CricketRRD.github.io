<h2>How Can I Help?</h2>

You can help Cricket grow and continue to be useful to
people in lots of ways. Perhaps the best way to get started
is to help provide support to new users on the mailing lists.

<p>
When you are ready to start hacking Cricket itself, another good
place to start is documentation. Take a look at the list of bugs
on SourceForge. Experiment with the filters until you get a list
of the documentation bugs. Choose one, assign it to yourself, and
then type away. If you've made a change
locally that you'd like to share, talk about it some on the Cricket
developers mailing list, and when people agree it's a good idea,
check it in.

