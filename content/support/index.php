If you are having a problem with Cricket you can get help from
these places:

<ul>
	<li>the <a href="doc">documentation</a>
	<li>the <a href="FAQ">FAQ</a>
	<li>other users, via the mailing lists
	<li>companies who offer <a href="commercial.php">Cricket support</a>
</ul>

<p>
Most people who have asked me for help will tell you that
they got better help, faster, from the mailing list. I try to
keep an eye on the list to help out on hard problems, but
I do not have the time to do 100% of the Cricket support.
Please take part in the mailing list community, first by
reading, then by asking, and finally by contributing answers
when you know them.
	
<p>
There are four mailing lists related to Cricket:

<ul>
	<li><a href="http://lists.sourceforge.net/mailman/listinfo/cricket-users">cricket-users@lists.sourceforge.net</a>
	<li><a href="http://lists.sourceforge.net/mailman/listinfo/cricket-announce">cricket-announce@lists.sourceforge.net</a>
	<li><a href="http://lists.sourceforge.net/mailman/listinfo/cricket-developers">cricket-developers@lists.sourceforge.net</a>
	<li><a href="http://lists.sourceforge.net/mailman/listinfo/cricket-commits">cricket-commits@lists.sourceforge.net</a>
</ul>

<p>
You can subscribe to a list by clicking on one of the links
above and following the instructions. Please note, your mailling
list password <b>will</b> be sent to you on a regular basis,
so choose a simple throwaway one.

<p>
The users list is for open discussion of Cricket, how to
use it, and how to extend it. The announce list is a
low-traffic, moderated list that lets maintainers announce new
versions of Cricket and other packages related to Cricket.
The developers and commits lists are for messages about
hacking the next version of Cricket. Only the hardcore
geeks need to subscribe to those lists.

